public class Figure3 {

//Variables
    private int x;
    private int y;
    private int width;
    private int height;

//Constructors
    public Figure3() {
        this.x = 0;
        this.y = 0;
        this.width = 0;
        this.height = 0;
    }
    
//Getters & Setters
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }    
} //Class End

